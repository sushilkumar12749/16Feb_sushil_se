#include <iostream>
using namespace std;

int main()
{
  double firstNumber, secondNumber;

  cout << "Enter the first number: " << endl;
  cin >> firstNumber;

  cout << "Enter the second number: " << endl;
  cin >> secondNumber;

  cout << "firstNumber + secondNumber = " << firstNumber + secondNumber << endl;

  cout << "firstNumber - secondNumber = " << firstNumber - secondNumber << endl;

  cout << "firstNumber * secondNumber = " << firstNumber * secondNumber << endl;

  cout << "firstNumber/secondNumber = " << firstNumber / secondNumber << endl;
}
